void Send_data(USART_TypeDef * USARTx,u8 *s)
{
	static char send_count=0;
	send_count=sizeof(s)/sizeof(s[0]);//读取传入的数组有几位
	while(send_count)
	{
		while(USART_GetFlagStatus(USARTx,USART_FLAG_TC )==RESET);	
		USART_SendData(USARTx,*s);
		s++;
		send_count--;
	}
}

